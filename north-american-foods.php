		     <h3>Mexican Foods</h3>
				<img src="images/mexican.jpg" alt="Mexican food">
			<p>
				Mexican cuisine is primarily a fusion of indigenous Mesoamerican cooking with European, especially Spanish, elements added after the Spanish conquest of the Aztec Empire in the 16th century. The staples are native foods, such as corn, beans, avocados, tomatoes, and chili peppers, along with rice, which was brought by the Spanish. Europeans introduced a large number of other foods, the most important of which were meats from domesticated animals (beef, pork, chicken, goat, and sheep), dairy products (especially cheese), and various herbs and spices.

				Over the centuries, this resulted in regional cuisines based on local conditions, such as those in Oaxaca, Veracruz and the Yucatán Peninsula. Mexican cuisine is an important aspect of the culture, social structure and popular traditions of Mexico. The most important example of this connection is the use of mole for special occasions and holidays, particularly in the South and Center regions of the country. For this reason and others, traditional Mexican cuisine was inscribed in 2010 on the Representative List of the Intangible Cultural Heritage of Humanity by UNESCO.[2]

				The best known of Mexico’s street foods is the taco, whose origin is based on the pre-Hispanic custom of picking up other foods with tortillas as utensils were not used.[10] The origin of the word is in dispute, with some saying it is derived from Nahuatl and others from various Spanish phrases.[33] Tacos are not eaten as the main meal; they are generally eaten before midday or late in the evening. Just about any other foodstuff can be wrapped in a tortilla, and in Mexico, it varies from rice, to meat (plain or in sauce), to cream, to vegetables, to cheese, or simply with plain chile peppers or fresh salsa. Preferred fillings vary from region to region with pork generally found more often in the center and south, beef in the north, seafood along the coasts, and chicken and lamb in most of the country.[34]
